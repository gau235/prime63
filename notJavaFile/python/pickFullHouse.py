三條張數= 3 # 設定甲輪抓幾張
一對張數= 2 # 設定乙輪抓幾張
#######################就像時針分針
def 甲輪抓牌(甲輪的收集, 甲輪第幾層):

	if 甲輪第幾層 == len(甲輪牌箱):
		 if len(甲輪的收集) == 三條張數:
		 	if(甲輪的收集[0][-1:]==甲輪的收集[1][-1:]==甲輪的收集[2][-1:]):
		 		
		 		print("抓葫蘆 "+ str(甲輪的收集))
		 		剩下那些=排除元素(甲輪牌箱, 甲輪的收集)
		 		乙輪抓牌(剩下那些, [], 0) # 再抓一輪
		 		
		 return # 上帝指示

	甲輪抓牌(甲輪的收集, 甲輪第幾層+1) # 問抓不抓? 不抓
	拷貝那份=甲輪的收集.copy()
	拷貝那份.append(甲輪牌箱[甲輪第幾層]) # 加進去
	甲輪抓牌(拷貝那份, 甲輪第幾層+1) # 問抓不抓? 抓
