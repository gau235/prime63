def 乙輪抓牌(乙輪牌箱, 乙輪的收集, 乙輪第幾層):
		
	if 乙輪第幾層 == len(乙輪牌箱):
		 if len(乙輪的收集) == 一對張數:
		 	if(乙輪的收集[0][-1:]==乙輪的收集[1][-1:]):
		 		
		 		print(' 搭配 ' + str(乙輪的收集))
		 
		 return # 上帝指示

	乙輪抓牌(乙輪牌箱, 乙輪的收集, 乙輪第幾層+1) # 問抓不抓? 不抓		
	拷貝那份=乙輪的收集.copy()
	拷貝那份.append(乙輪牌箱[乙輪第幾層]) # 加進去
	乙輪抓牌(乙輪牌箱, 拷貝那份, 乙輪第幾層+1) # 問抓不抓? 抓
